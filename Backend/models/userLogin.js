import mongoose from 'mongoose';

const loginSchema = new mongoose.Schema(
    {
        username: {
            type: String,
            required: true,
        },
        email: {
            type: String,
            required: true,
        },
        password: {
            type: String,
            required: true,
        }
    }
);

export const LoginData = mongoose.model('LoginData', loginSchema);